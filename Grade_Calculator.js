const marks = 52; 

if (marks <= 59) {
    console.log("F");
} 
else if (marks <= 69.9) {
    console.log("D");
} 
else if (marks <= 79.9) {
    console.log("C");
} 
else if (marks <= 89.9) {
    console.log("B");
} 
else if (marks <= 100) {
    console.log("A");
} 
else {
    console.log("Invalid marks");
}